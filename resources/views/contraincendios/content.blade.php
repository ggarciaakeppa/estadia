<h1 id="h1-contraincendios">CONTRAINCENDIOS</h1>
<p id="p1-contraincendios">En BOMOC contamos con las principales marcas de equipos integrados 
    contraincendios, como Barmesa, WDM, Aurora PICSA, Peerless Pump, entre otras.
</p><br>
<p id="p1-contraincendios">
    Todos los equipos integrados cuentan con garantía, entre 1 y 3 años 
    (dependiendo del fabricante) directo con fabrica, algunos de los equipos 
    cuentan con certificado de pruebas hidrostáticas y comportamiento, certificado 
    de calidad y sello de autenticidad.
</p><br>
<p id="p1-contraincendios">
    También contamos con sistemas contra incendio listados UL y aprobados 
    FM, los cuales deben de apegarse a estrictas normas de diseño y operación 
    de protección contra incendio (NFPA 20).
</p><br>
